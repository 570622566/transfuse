package org.androidrobotics.gen;

/**
 * @author John Ericksen
 */
public class ActivityDescriptor {

    private String name;
    private int layout;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLayout() {
        return layout;
    }

    public void setLayout(int layout) {
        this.layout = layout;
    }
}
